package ego.util;


public class util 
{
	public String Dosplit(String val)
	{
		String ary[] = val.split("@");
		
		return ary[0];	
	}
	
	public int StringToInt(String value)
	{
		int intValue = Integer.parseInt(value);
		return intValue;
	}
	public String IntToString(int value)
	{
		String stringValue = Integer.toString(value); 
		return stringValue;
	}
	
	public static void main(String[] args) 
	{
		
		/** String을 int로*******************************/
		String s = "145";
		
		util u = new util();
		int a = u.StringToInt(s);
		System.out.println(a + 12);
		
		/** int를 String으로*******************************/
		int i = 50;
		
		util uint = new util();
		String test = uint.IntToString(i);
		System.out.println(test + 50);
		

	}
}
